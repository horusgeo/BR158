package horusgeo.br158;


public class Confrontantes {

    private String direita;
    private String esquerda;
    private String frente;
    private String fundos;
    private Integer id;

    Confrontantes(){

    }

    public String getDireita() {
        return direita;
    }

    public void setDireita(String direita) {
        this.direita = direita;
    }

    public String getEsquerda() {
        return esquerda;
    }

    public void setEsquerda(String esquerda) {
        this.esquerda = esquerda;
    }

    public String getFrente() {
        return frente;
    }

    public void setFrente(String frente) {
        this.frente = frente;
    }

    public String getFundos() {
        return fundos;
    }

    public void setFundos(String fundos) {
        this.fundos = fundos;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }
}
